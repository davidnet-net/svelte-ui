    `//! This file is autogenerated! Edit in meta/scripts/GenerateMetaData.ts\n\n` +
    `export const metadata = {\n` +
    `  version: "${version}"\n` +
    `};\n`;

writeFileSync('./src/lib/metadata.ts', content);
